"""
for else:
for 临时变量 in 序列  -- 临时变量
    重复执行的代码
else:
    循环正常结束
1. 书写道歉的循环
2.循环正常结束要执行的代码 -- else
"""

# 定义一个字符串变量 str1
str1 = 'itheima'

# 使用 for 循环遍历 str1 中的每个字符
for i in str1:
    print(i)  # 打印当前循环中的字符

# 当 for 循环正常结束（没有遇到 break 退出）时，执行 else 代码块
else:
    print('循环正常执行')  # 打印提示信息，表示循环正常结束

# 额外要求：使用 for-else 循环实现特定的功能
# 定义一个循环，重复打印 "媳妇儿，我错了" 五次
for num in range(1, 6):  # range(1, 6) 生成一个序列，从 1 到 5
    print(f"{num}. 媳妇儿，我错了")  # 打印消息，num 表示循环的当前次数

# 当上述循环正常结束时，执行以下代码
else:
    print("媳妇儿原谅我了")  # 循环结束后打印，表示得到了媳妇儿的原谅
